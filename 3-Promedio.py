#calcular promedio de 4 notas
no1 = float(input("ingrese la primera nota: \n"))
no2 = float(input("ingrese la segunda nota: \n"))
no3 = float(input("ingrese la tercera nota: \n"))
no4 = float(input("ingrese la cuarta nota: \n"))
prom = (no1 + no2 + no3 + no4) / 4
print(f"el promedio de las 4 notas es {prom}")